public class Cartela {

    private String nome;
    private int um, dois, tres, quatro, cinco, seis;

    public Cartela(String nome, int um, int dois, int tres, int quatro, int cinco, int seis) {
        this.nome = nome;
        this.um = um;
        this.dois = dois;
        this.tres = tres;
        this.quatro = quatro;
        this.cinco = cinco;
        this.seis = seis;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getUm() {
        return um;
    }

    public void setUm(int um) {
        this.um = um;
    }

    public int getDois() {
        return dois;
    }

    public void setDois(int dois) {
        this.dois = dois;
    }

    public int getTres() {
        return tres;
    }

    public void setTres(int tres) {
        this.tres = tres;
    }

    public int getQuatro() {
        return quatro;
    }

    public void setQuatro(int quatro) {
        this.quatro = quatro;
    }

    public int getCinco() {
        return cinco;
    }

    public void setCinco(int cinco) {
        this.cinco = cinco;
    }

    public int getSeis() {
        return seis;
    }

    public void setSeis(int seis) {
        this.seis = seis;
    }

    
}
